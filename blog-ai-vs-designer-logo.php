                <header class="intel-header">
                    <div class="intel-meta">Brand Strategy // Advice</div>
                    <h1 class="intel-title">Got $500 for a logo? Why the AI vs. designer debate is missing the point.</h1>
                    <p class="intel-subtitle">A tight budget forces a tough choice: fire up an AI generator or hire a human? We break down the real risks and hidden costs of AI branding for NZ businesses.</p>
