        var targetDate = new Date(2026, 1, 28, 9, 0, 0); 

        function isNZHoliday(date) {
            const year = date.getFullYear();
            const month = date.getMonth(); 
            const day = date.getDate();
            
            const holidays = [
                { m: 0, d: 1 },  
                { m: 0, d: 2 },  
                { m: 1, d: 6 },  
                { m: 3, d: 3 },  
                { m: 3, d: 6 },  
