                <span style="color: var(--signal-med); font-size: 0.95rem;">Are you selling food, alcohol, or providing a transport service? If yes, check what specific council or government licenses are required before you can legally operate in your area.</span>
              </div>
            </div>

            <div class="check-item">
              <span class="check-icon">2</span>
              <div style="font-size: 1.05rem;">
